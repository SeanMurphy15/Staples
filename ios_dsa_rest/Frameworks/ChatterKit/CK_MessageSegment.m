/////////////////////////////////////////////////////////////////////////
@implementation CK_ChatterKitObject (CK_MessageSegment)

- (NSInteger) messageSegmentType
{
    NSInteger result = CK_MessageSegmentType_Unknown;
    
    NSString* type = [self objectForKey:@"type"];
    
    if ([type isEqualToString:@"EntityLink"])
    {
        result = CK_MessageSegmentType_EntityLink;
    }
    else if ([type isEqualToString:@"FieldChange"])
    {
        result = CK_MessageSegmentType_FieldChange;
    }
    else if ([type isEqualToString:@"FieldChangeName"])
    {
        result = CK_MessageSegmentType_FieldChangeName;
    }
    else if ([type isEqualToString:@"FieldChangeValue"])
    {
        result = CK_MessageSegmentType_FieldChangeValue;
    }
    else if ([type isEqualToString:@"Hashtag"])
    {
        result = CK_MessageSegmentType_Hashtag;
    }
    else if ([type isEqualToString:@"Link"])
    {
        result = CK_MessageSegmentType_Link;
    }
    else if ([type isEqualToString:@"Mention"])
    {
        result = CK_MessageSegmentType_Mention;
    }
    else if ([type isEqualToString:@"MoreChanges"])
    {
        result = CK_MessageSegmentType_MoreChanges;
    }
    else if ([type isEqualToString:@"Text"])
    {
        result = CK_MessageSegmentType_Text;
    }
    
    return result;
}

@end

