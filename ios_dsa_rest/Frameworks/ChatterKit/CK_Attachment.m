#import "CK_Attachment.h"

@interface CK_Attachment ()

@property (nonatomic, assign) NSInteger attachmentType;

@end

@implementation CK_ChatterKitObject (CK_Attachment)
- (NSInteger) attachmentType
{
    NSInteger result = CK_AttachmentType_Unknown;
    
    if ([self objectForKey:@"url"] != nil) //link
    {
        result = CK_AttachmentType_Link;
    }
    else if ([self objectForKey:@"mimeType"] != nil) //content
    {
        result = CK_AttachmentType_Content;
    }
    else if ([self objectForKey:@"dashboardId"] != nil) //dashboard
    {
        result = CK_AttachmentType_Dashboard;
    }
    else if ([self objectForKey:@"text"] != nil) //case comment
    {
        result = CK_AttachmentType_CaseComment;
    }
    
    return result;
}
@end

@implementation CK_Attachment

@end

